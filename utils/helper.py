import sys
import os


def clearShell() -> None:
    windows = ["win32", "cygwin"]
    os.system("cls" if sys.platform in windows else "clear")


def canContinue() -> bool:
    userInput = str.lower(input("Press any key to continue or (Q)uit"))
    return False if userInput == "q" else True


